CREATE TABLE person (
    id INT GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR UNIQUE NOT NULL,
    year INT CHECK (year > 0 AND year < 2023)
);

CREATE TABLE book (
    id INT GENERATED BY DEFAULT AS IDENTITY,
    title VARCHAR NOT NULL,
    author VARCHAR NOT NULL,
    year INT CHECK (year > 0 AND year < 2023),
    holder VARCHAR REFERENCES person(name) ON DELETE SET NULL
)